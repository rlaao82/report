const LIKERT_ORDER = ["매우 그렇다","그렇다","보통이다","그렇지 않다","매우 그렇지 않다","전혀 그렇지 않다"];

// X축: 응답지, 시리즈: 조직 또는 직군
function buildLikertChartData(qData, dimension) {
  const groups = Object.keys(qData[dimension]);
  const keys = LIKERT_ORDER.filter(k => k !== "매우 그렇지 않다");
  return keys.map(answerKey => {
    const row = { name: answerKey };
    groups.forEach(group => {
      const total = LIKERT_ORDER.reduce((s,k) => s + (qData[dimension][group][k]||0), 0);
      const cnt = qData[dimension][group][answerKey] || 0;
      row[group] = cnt;
      row[`${group}_pct`] = total > 0 ? Math.round(cnt / total * 100) : 0;
    });
    return row;
  });
}

function buildOtherChartData(qData, dimension) {
  const groups = Object.keys(qData[dimension]);
  const order = qData.levelOrder;
  const shortLabels = qData.levelShort || {};
  return order.map(answerKey => {
    const row = { name: shortLabels[answerKey] || answerKey, fullName: answerKey };
    groups.forEach(group => {
      const vals = qData[dimension][group];
      const total = Object.values(vals).reduce((s,v)=>s+v,0);
      const cnt = vals[answerKey] || 0;
      row[group] = cnt;
      row[`${group}_pct`] = total > 0 ? Math.round(cnt / total * 100) : 0;
    });
    return row;
  });
}



