function buildLeaderLikertData(qData, dimension) {
  const groups = Object.keys(qData[dimension]);
  const keys = LEADER_LIKERT_ORDER.filter(k => k !== "매우 그렇지 않다");
  return keys.map(ans => {
    const row = { name: ans };
    groups.forEach(g => {
      const total = LEADER_LIKERT_ORDER.reduce((s,k) => s+(qData[dimension][g][k]||0), 0);
      const cnt = qData[dimension][g][ans] || 0;
      row[g] = cnt;
      row[`${g}_pct`] = total > 0 ? Math.round(cnt/total*100) : 0;
    });
    return row;
  });
}

function buildLeaderSingleData(qData, dimension) {
  const groups = Object.keys(qData[dimension]);
  const sl = qData.shortLabels || {};
  return qData.order.map(ans => {
    const row = { name: sl[ans] || ans, fullName: ans };
    groups.forEach(g => {
      const vals = qData[dimension][g];
      const total = Object.values(vals).reduce((s,v)=>s+v, 0);
      const cnt = vals[ans] || 0;
      row[g] = cnt;
      row[`${g}_pct`] = total > 0 ? Math.round(cnt/total*100) : 0;
    });
    return row;
  });
}

